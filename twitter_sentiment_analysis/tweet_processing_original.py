tok = Tokenizer(preserve_case=False)


def tokenize_tweet(s):
    '''
    :s : string; a tweet
    :return : list; words that don't contain url, @somebody, and in utf-8 and lower case
    '''
    words = tok.tokenize(s.replace('.', '. '))
